
function A_C = find_AC(cc)
    % compressive anisotropy A_C = U_AC / (B_DC * |G_DC|)^1/2
    B_dc = find_bulk_dc(cc);
    [G_ac, G_dc] = find_acdc_eps4(cc);
    [U_ac, U_dc] = find_acdc_eps1(cc);

    A_C = sqrt(U_ac)./sqrt(abs(B_dc.*G_dc));
end

function A_G = find_AG(cc)
    % shear anisotropy A_C = G_AC / G_DC
    [G_ac, G_dc] = find_acdc_eps4(cc);
    [U_ac, U_dc] = find_acdc_eps1(cc);

    A_G = sqrt(G_AC)/abs(G_DC);
end

function AU = find_AU(c_mat)
    % calculate universal anisotropy index A^U
    [KR, GR] = find_reuss(c_mat);
    [KV, GV] = find_voigt(c_mat);

    AU = 5*(GV/GR) + (KV/KR) - 6;
end

function B_dc = find_bulk_dc(cc)
    B_dc = (1/9).*(cc(1,1)+cc(2, 2)+cc(3,3)+2.*cc(1,2)+2.*cc(1,3)+2.*cc(2,3));
end

function [KR, GR] = find_reuss(c_mat)
    c_mat = fill_in_ut(c_mat);
    s_mat = inv(c_mat); % compliance
    kr1 = (s_mat(1,1) + s_mat(2,2) + s_mat(3, 3)) + 2.*(s_mat(1,2)+s_mat(1,3)+s_mat(2,3));
    KR = 1/kr1;

    gr1 = 4.*(s_mat(1,1) + s_mat(2,2) + s_mat(3, 3)) - 4.*(s_mat(1,2) + s_mat(1,3) + s_mat(2, 3)) + 3.*(s_mat(4,4) + s_mat(5,5) + s_mat(6, 6));
    GR = 15./gr1;
end

function [KV, GV] = find_voigt(c_mat)
    c_mat = fill_in_ut(c_mat);
    KV = (c_mat(1,1) + c_mat(2,2) + c_mat(3, 3)) + 2.*(c_mat(1,2)+c_mat(1,3)+c_mat(2,3));
    KV = KV ./ 9;

    GV = (c_mat(1,1)+c_mat(2,2)+c_mat(3,3))-(c_mat(1,2)+c_mat(1,3)+c_mat(2,3))+3.*(c_mat(4,4)+c_mat(5,5)+c_mat(6,6));
    GV = GV ./ 15;
end


